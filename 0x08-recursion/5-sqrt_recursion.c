/**
 * _sqrt_recursion - Function that finds the square
 *			root of an integer
 * @n: The number with ehich we are finding it sqrt.
 */

int _sqrt_recursion(int n)
{
	int sqrt();
	int squareRoot = sqrt(n); 

	if (!(squareRoot))
		return (-1);

	return (squareRoot);
}
